
<template>
<div>
  <h1>Select a date and time:</h1>
  <div class="wrapper">
    <div class="search">
      <form class="pure-form">
        <i class="fas fa-search"></i><input v-model="searchText" />
      </form>
    </div>
  </div>
  <DatesList :dates="dates" />
</div>
</template>


<script>
import DatesList from "../components/DatesList.vue"
export default {
  name: 'HomeView',
  components: {
    DatesList
  },
  data() {
    return {
      searchText: '',
    }
  },
  computed: {
    dates() {
      return this.$root.$data.dates.filter(date => date.day.toLowerCase().search(this.searchText.toLowerCase()) >= 0);
    }
  },
}
</script>


